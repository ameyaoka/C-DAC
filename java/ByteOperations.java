public class ByteOperations {
    public static void main(String[] args) {
        // a. The number of bits used to represent a byte value
        int bitsInByte = Byte.SIZE;
        System.out.println("a. The number of bits used to represent a byte value: " + bitsInByte);

        // b. The number of bytes used to represent a byte value
        int bytesInByte = 1;
        System.out.println("b. The number of bytes used to represent a byte value: " + bytesInByte);

        // c. The minimum value a byte
        byte minByteValue = Byte.MIN_VALUE;
        System.out.println("c. The minimum value a byte: " + minByteValue);

        // d. The maximum value a byte
        byte maxByteValue = Byte.MAX_VALUE;
        System.out.println("d. The maximum value a byte: " + maxByteValue);
    }
}

